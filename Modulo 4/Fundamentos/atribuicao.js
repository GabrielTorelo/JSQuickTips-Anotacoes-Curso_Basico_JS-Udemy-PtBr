const a = 7 // Atribuindo o valor 7 a constante 'a'
let b = 3 // Atribuindo o valor 3 a variável 'b' do tipo 'let'

// Atribuição com operadores aritméticos 
// Sempre a operação vem 1º e depois o sinal de atribuição (=)
b += a // b = b + a
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 6 // b = b / 6
console.log(b)

b %= 2 // b = b % 2
console.log(b)